package edu.nwmissouri.isl.aasis.case.ec_plan.ManageGame;

import edu.ksu.cis.macr.obaa_pp.ec.base.IExecutor;
import edu.ksu.cis.macr.obaa_pp.ec.plans.IExecutablePlan;
import edu.ksu.cis.macr.obaa_pp.ec.plans.IPlanState;
import edu.ksu.cis.macr.organization.model.InstanceGoal;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

/**
 * The last step in the plan. It allows for any functionality needed when exiting the plan.
 */
public enum ManageGame_Stop implements IPlanState<ManageGame_Plan> {
  INSTANCE;

  private static final Logger LOG = LoggerFactory.getLogger(ManageGame_Stop.class);

  @Override
  public synchronized void Enter(final IExecutablePlan plan, final IExecutor ec, final InstanceGoal<?> ig) {
    // Nothing
  }

  @Override
  public synchronized void Execute(final IExecutablePlan plan, final IExecutor ec, final InstanceGoal<?> ig) {
    plan.setDone(true);
    LOG.info("Congratulations - Three in a row!");
    System.exit(0);
  }

  @Override
  public synchronized void Exit(final IExecutablePlan plan, final IExecutor ec, final InstanceGoal<?> ig) {
    // Nothing
  }
}
